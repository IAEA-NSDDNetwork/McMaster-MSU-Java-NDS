    private void OKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OKActionPerformed
        drw.setDrawn(true);
        drw.setPortrait(portrait);
        
        boolean wrongFormat=false;
        
        try{
            nPages=Integer.parseInt(pageField.getText());
        }catch(Exception e){
        	wrongFormat=true;
        }
        
        //debug
        //System.out.println("In includeDrawing: line 155: nPages="+nPages+" wrongFormat="+wrongFormat);
        
        if(wrongFormat || nPages<0)
        	JOptionPane.showMessageDialog(this,"Please enter a positive,integer number");
        else{
            drw.setPages(nPages);        
            drawingAdded=true;
            NDSControl.isBandSettingModified=true;
        	dispose();
        }

    }//GEN-LAST:event_OKActionPerformed

