    private void pageFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pageFieldActionPerformed
        int i=0;
        boolean wrongFormat=false;
        try{
            nPages=Integer.parseInt(pageField.getText());
        }catch(Exception e){
        	wrongFormat=true;
        }
        
        if(wrongFormat || nPages<0)
        	JOptionPane.showMessageDialog(this,"Please enter a positive,integer number");
        
        nPages=i;
        

        
    }//GEN-LAST:event_pageFieldActionPerformed

